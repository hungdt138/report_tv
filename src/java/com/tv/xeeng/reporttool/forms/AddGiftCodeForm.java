package com.tv.xeeng.reporttool.forms;

import org.apache.struts.action.ActionForm;

public class AddGiftCodeForm extends ActionForm {
	private int value;
	private int total;
    private String type;
	
	public AddGiftCodeForm() {
	}
	public AddGiftCodeForm(int value, int total) {
	
		this.value = value;
		this.total = total;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}


    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
