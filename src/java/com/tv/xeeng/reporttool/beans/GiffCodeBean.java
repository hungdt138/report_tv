package com.tv.xeeng.reporttool.beans;

import java.sql.Date;
import java.sql.Time;

public class GiffCodeBean {

    private int giffCodeId;
    private String serial;
    private Date createdDate;
    private Time createdTime;
    private Date usedDate;
    private Time usedTime;
    private int bonusMoney;
    private String type;
    private int marketingChannelId;
    private int totalPage;
    private int totalRecord;

    public GiffCodeBean() {

    }

    public GiffCodeBean(int giffCodeId, String serial, Date createdDate,
            Time createdTime, Date usedDate, Time usedTime, int bonusMoney,
            int marketingChannelId, int totalPage, int totalRecord) {
        super();
        this.giffCodeId = giffCodeId;
        this.serial = serial;
        this.createdDate = createdDate;
        this.createdTime = createdTime;
        this.usedDate = usedDate;
        this.usedTime = usedTime;
        this.bonusMoney = bonusMoney;
        this.marketingChannelId = marketingChannelId;
        this.totalPage = totalPage;
        this.totalRecord = totalRecord;
    }

    public Time getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(Time createdTime) {
        this.createdTime = createdTime;
    }

    public Time getUsedTime() {
        return usedTime;
    }

    public void setUsedTime(Time usedTime) {
        this.usedTime = usedTime;
    }

    public int getTotalPage() {
        return totalPage;
    }

    public void setTotalPage(int totalPage) {
        this.totalPage = totalPage;
    }

    public int getTotalRecord() {
        return totalRecord;
    }

    public void setTotalRecord(int totalRecord) {
        this.totalRecord = totalRecord;
    }

    public int getGiffCodeId() {
        return giffCodeId;
    }

    public void setGiffCodeId(int giffCodeId) {
        this.giffCodeId = giffCodeId;
    }

    public String getSerial() {
        return serial;
    }

    public void setSerial(String serial) {
        this.serial = serial;
    }

    public Date getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(Date createdDate) {
        this.createdDate = createdDate;
    }

    public Date getUsedDate() {
        return usedDate;
    }

    public void setUsedDate(Date usedDate) {
        this.usedDate = usedDate;
    }

    public int getBonusMoney() {
        return bonusMoney;
    }

    public void setBonusMoney(int bonusMoney) {
        this.bonusMoney = bonusMoney;
    }

    public int getMarketingChannelId() {
        return marketingChannelId;
    }

    public void setMarketingChannelId(int marketingChannelId) {
        this.marketingChannelId = marketingChannelId;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
