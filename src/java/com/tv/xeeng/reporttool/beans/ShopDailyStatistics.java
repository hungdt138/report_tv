package com.tv.xeeng.reporttool.beans;

import java.util.ArrayList;
import java.util.List;

/**
 * Created by yeuchimse on 12/06/2014.
 */
public class ShopDailyStatistics {
    public final static int TOTAL_XEENG_ID = 10001;
    public final static int TOTAL_GOLD_ID = 10002;

    private String reportDate;
    private List<Integer> itemCounts;
    private int totalXeeng;
    private int totalGold;

    public ShopDailyStatistics() {
        this.itemCounts = new ArrayList<Integer>();
    }

    public String getReportDate() {
        return reportDate;
    }

    public void setReportDate(String reportDate) {
        this.reportDate = reportDate;
    }

    public List<Integer> getItemCounts() {
        return itemCounts;
    }

    public void setItemCounts(List<Integer> itemCounts) {
        this.itemCounts = itemCounts;
    }

    public int getTotalXeeng() {
        return totalXeeng;
    }

    public void setTotalXeeng(int totalXeeng) {
        this.totalXeeng = totalXeeng;
    }

    public int getTotalGold() {
        return totalGold;
    }

    public void setTotalGold(int totalGold) {
        this.totalGold = totalGold;
    }
}
